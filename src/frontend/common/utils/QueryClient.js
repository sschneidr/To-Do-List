import { QueryClient, QueryCache } from "@tanstack/react-query";

const queryClient = new QueryClient();

const queryCache = new QueryCache({
  onError: (error) => {
    console.log(error);
  },
  onSuccess: (data) => {
    console.log(data);
  },
  onSettled: (data, error) => {
    console.log(data, error);
  },
});

export default queryClient;

